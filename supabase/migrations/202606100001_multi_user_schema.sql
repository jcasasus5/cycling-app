create table public.routes (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  name text not null,
  distance_km double precision not null,
  elevation_gain_m double precision not null,
  start_altitude_m double precision not null,
  end_altitude_m double precision not null,
  avg_grade_percent double precision not null,
  max_grade_percent double precision not null,
  created_at timestamptz not null default now(),
  original_image_path text
);

create table public.route_segments (
  id bigint generated by default as identity primary key,
  route_id bigint not null references public.routes(id) on delete cascade,
  start_km double precision not null,
  end_km double precision not null,
  grade_percent double precision not null,
  start_altitude_m double precision not null,
  end_altitude_m double precision not null
);

create table public.activities (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  route_id bigint not null references public.routes(id) on delete cascade,
  started_at timestamptz not null,
  ended_at timestamptz not null,
  status text not null check (status in ('completed', 'partial')),
  active_seconds integer not null,
  total_seconds integer not null,
  distance_km double precision not null,
  avg_power_w integer not null,
  max_power_w integer not null,
  avg_cadence_rpm integer not null,
  avg_speed_kph double precision not null,
  completed_elevation_m double precision not null
);

create table public.activity_samples (
  id bigint generated by default as identity primary key,
  activity_id bigint not null references public.activities(id) on delete cascade,
  timestamp_ms bigint not null,
  elapsed_seconds integer not null,
  km double precision not null,
  speed_kph double precision not null,
  cadence_rpm integer not null,
  power_w integer not null,
  grade_percent double precision not null,
  altitude_m double precision not null,
  paused boolean not null
);

create table public.user_settings (
  user_id uuid primary key default auth.uid() references auth.users(id) on delete cascade,
  openai_api_key_encrypted text,
  max_trainer_grade_percent double precision not null default 16,
  enable_negative_grades boolean not null default true,
  smooth_grade_changes boolean not null default true,
  rider_weight_kg double precision not null default 75,
  bike_weight_kg double precision not null default 9,
  updated_at timestamptz not null default now()
);

create table public.imported_images (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  path text not null,
  created_at timestamptz not null default now()
);

create index routes_user_created_idx on public.routes (user_id, created_at desc);
create index route_segments_route_idx on public.route_segments (route_id, start_km);
create index activities_user_started_idx on public.activities (user_id, started_at desc);
create index activities_route_idx on public.activities (route_id);
create index activity_samples_activity_idx on public.activity_samples (activity_id, elapsed_seconds);
create index imported_images_user_idx on public.imported_images (user_id);

