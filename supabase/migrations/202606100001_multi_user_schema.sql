create policy routes_owner_all on public.routes
  for all to authenticated
  using ((select auth.uid()) = user_id)
  with check ((select auth.uid()) = user_id);

create policy route_segments_owner_all on public.route_segments
  for all to authenticated
  using (exists (
    select 1 from public.routes
    where routes.id = route_segments.route_id
      and routes.user_id = (select auth.uid())
  ))
  with check (exists (
    select 1 from public.routes
    where routes.id = route_segments.route_id
      and routes.user_id = (select auth.uid())
  ));

create policy activities_owner_all on public.activities
  for all to authenticated
  using ((select auth.uid()) = user_id)
  with check (
    (select auth.uid()) = user_id
    and exists (
      select 1 from public.routes
      where routes.id = activities.route_id
        and routes.user_id = (select auth.uid())
    )
  );

create policy activity_samples_owner_all on public.activity_samples
  for all to authenticated
  using (exists (
    select 1 from public.activities
    where activities.id = activity_samples.activity_id
      and activities.user_id = (select auth.uid())
  ))
  with check (exists (
    select 1 from public.activities
    where activities.id = activity_samples.activity_id
      and activities.user_id = (select auth.uid())
  ));

create policy user_settings_owner_all on public.user_settings
  for all to authenticated
  using ((select auth.uid()) = user_id)
  with check ((select auth.uid()) = user_id);

create policy imported_images_owner_all on public.imported_images
  for all to authenticated
  using ((select auth.uid()) = user_id)
  with check ((select auth.uid()) = user_id);

create or replace function public.create_route(draft jsonb)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  new_route_id bigint;
  segment jsonb;
begin
  if auth.uid() is null then
    raise exception 'authentication required';
  end if;

  insert into public.routes (
    user_id, name, distance_km, elevation_gain_m, start_altitude_m,
    end_altitude_m, avg_grade_percent, max_grade_percent, original_image_path
  ) values (
    auth.uid(),
    draft->>'name',
    (draft->>'distance_km')::double precision,
    (draft->>'elevation_gain_m')::double precision,
    (draft->>'start_altitude_m')::double precision,
    (draft->>'end_altitude_m')::double precision,
    (draft->>'avg_grade_percent')::double precision,
    (draft->>'max_grade_percent')::double precision,
    nullif(draft->>'original_image_path', '')
  )
  returning id into new_route_id;

  for segment in select value from jsonb_array_elements(draft->'segments')
  loop
    insert into public.route_segments (
      route_id, start_km, end_km, grade_percent, start_altitude_m, end_altitude_m
    ) values (
      new_route_id,
      (segment->>'start_km')::double precision,
      (segment->>'end_km')::double precision,
      (segment->>'grade_percent')::double precision,
      (segment->>'start_altitude_m')::double precision,
      (segment->>'end_altitude_m')::double precision
    );
  end loop;

  return new_route_id;
end;
$$;

create or replace function public.update_route(target_route_id bigint, draft jsonb)
returns boolean
language plpgsql
security invoker
set search_path = public
as $$
declare
  segment jsonb;
begin
  update public.routes set
    name = draft->>'name',
    distance_km = (draft->>'distance_km')::double precision,
    elevation_gain_m = (draft->>'elevation_gain_m')::double precision,
    start_altitude_m = (draft->>'start_altitude_m')::double precision,
    end_altitude_m = (draft->>'end_altitude_m')::double precision,
    avg_grade_percent = (draft->>'avg_grade_percent')::double precision,
    max_grade_percent = (draft->>'max_grade_percent')::double precision,
    original_image_path = nullif(draft->>'original_image_path', '')
  where id = target_route_id and user_id = auth.uid();

  if not found then
    return false;
  end if;

  delete from public.route_segments where route_id = target_route_id;
  for segment in select value from jsonb_array_elements(draft->'segments')
  loop
    insert into public.route_segments (
      route_id, start_km, end_km, grade_percent, start_altitude_m, end_altitude_m
    ) values (
      target_route_id,
      (segment->>'start_km')::double precision,
      (segment->>'end_km')::double precision,
      (segment->>'grade_percent')::double precision,
      (segment->>'start_altitude_m')::double precision,
      (segment->>'end_altitude_m')::double precision
    );
  end loop;
  return true;
end;
$$;

create or replace function public.duplicate_route(target_route_id bigint)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  source_route public.routes%rowtype;
  new_route_id bigint;
begin
  select * into source_route
  from public.routes
  where id = target_route_id and user_id = auth.uid();

  if not found then
    return null;
  end if;

  insert into public.routes (
    user_id, name, distance_km, elevation_gain_m, start_altitude_m,
    end_altitude_m, avg_grade_percent, max_grade_percent, original_image_path
  ) values (
    auth.uid(), source_route.name || ' copia', source_route.distance_km,
    source_route.elevation_gain_m, source_route.start_altitude_m,
    source_route.end_altitude_m, source_route.avg_grade_percent,
    source_route.max_grade_percent, source_route.original_image_path
  ) returning id into new_route_id;

  insert into public.route_segments (
    route_id, start_km, end_km, grade_percent, start_altitude_m, end_altitude_m
  )
  select new_route_id, start_km, end_km, grade_percent, start_altitude_m, end_altitude_m
  from public.route_segments
  where route_id = target_route_id;

  return new_route_id;
end;
$$;

